// @ts-check
const { test, expect } = require('../fixtures');
const config = require('../config');
const { expectWidgetsVisible, performTrackScenario } = require('../widget.helper');

// =============================================================================
// КОНФИГУРАЦИЯ ТЕСТОВ
// =============================================================================

/**
 * Получаем список партнёров из конфигурации
 * Каждый партнёр — это отдельная страница для тестирования
 * 
 * Пример партнёра:
 * {
 *   name: 'Scott Harvey Winery (staging)',
 *   url: 'https://staging.getwinespot.com/scottharveywinery-staging/index.html'
 * }
 */
const partners = config.partners;

// =============================================================================
// ГЕНЕРАЦИЯ ТЕСТОВ ДЛЯ КАЖДОГО ПАРТНЁРА
// =============================================================================

/**
 * Для каждого партнёра создаётся отдельная группа тестов
 * Это позволяет:
 * - Видеть в отчёте какой именно партнёр упал
 * - Запускать тесты для разных партнёров параллельно
 * - Легко добавить нового партнёра (просто добавить в config.js)
 */
partners.forEach((partner) => {
  /**
   * Группа тестов для одного партнёра
   * Название группы включает имя партнёра для удобства в отчёте
   */
  test.describe(`Partner: ${partner.name}`, () => {
    /**
     * Настраиваем таймаут для всех тестов в группе
     * TEST_TIMEOUT берётся из config.js (60 сек локально, 120 сек в CI)
     */
    test.describe.configure({ timeout: config.TEST_TIMEOUT });

    /**
     * beforeEach — выполняется перед каждым тестом в группе
     * Открываем страницу партнёра перед тестом
     */
    test.beforeEach(async ({ page }) => {
      await page.goto(partner.url, {
        waitUntil: 'domcontentloaded',  // Ждём загрузки DOM, не обязательно всех ресурсов
        timeout: 60000  // Максимальное время загрузки страницы
      });
    });

    // ========================================================================
    // ТЕСТ 1: Проверка видимости виджета
    // ========================================================================
    /**
     * Проверяет, что виджет корректно отображается на странице
     * 
     * Критерии успеха:
     * - Виджет найден в DOM (iframe #winespot)
     * - Виджет видим (display !== 'none', visibility !== 'hidden')
     * - position: fixed (виджет зафиксирован в углу экрана)
     * - Размеры больше минимальных (ширина и высота > 30px)
     * - Виджет полностью в пределах viewport
     * 
     * @see tests/widget.helper.js -> expectWidgetsVisible()
     */
    test('должен отображать виджет корректно', async ({ page }) => {
      await expectWidgetsVisible(page);
    });

    // ========================================================================
    // ТЕСТ 2: Проверка открытия чата и перехода к авторизации
    // ========================================================================
    /**
     * Проверяет полный сценарий взаимодействия с виджетом:
     * 1. Виджет отображается на странице
     * 2. Чат открывается (клик по медальке или виджету)
     * 3. Кнопка "Track and manage my orders" работает
     * 4. Появляется форма авторизации с полем email
     * 
     * Этот тест покрывает основной user flow:
     * Пользователь видит виджет → открывает чат → хочет отследить заказ → 
     * переходит к авторизации
     * 
     * @see tests/widget.helper.js -> performTrackScenario()
     */
    test('должен открывать чат и переходить на форму авторизации по кнопке Track', async ({ page }) => {
      // Шаг 1: Проверяем видимость виджета
      await expectWidgetsVisible(page);
      
      // Шаг 2: Открываем чат и нажимаем Track, ждём форму авторизации
      // performTrackScenario() внутри:
      // - Находит кнопку Track (или открывает чат через медальку)
      // - Кликает по кнопке Track
      // - Ждёт появления поля email (форма авторизации)
      await performTrackScenario(page);
    });
  });
});

